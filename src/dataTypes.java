public class dataTypes 
{
   public static void main(String[] args)
   {

       byte byte_val = 127;
       short short_val = 255;
       int int_val = 2550;
       long long_val = 1223l;
       
       float float_val = 3.141512f;
       double double_val = 5.8567;

       char char_val = '\u263A';

       boolean bool_val = true;

        System.out.println(byte_val);
        System.out.println(short_val);
        System.out.println(int_val);
        System.out.println(long_val);

        System.out.println(float_val);
        System.out.println(double_val);
        System.out.println(char_val);
        System.out.println(bool_val);
    }
}
