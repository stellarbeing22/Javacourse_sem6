public class operations 
{
    public static void main(String[] args)
    {
        //arithematic operators
        //+, -, *, /, %

        //compound assignment operator: +=, -=, *=, /=, %= 

        int num1 = 5; 
        int num2 = 10;
        int result = num1 + num2;

        //Additon
        result = num1 + num2; 
        System.out.println(result);

        //Subtraction
        result = num1 - num2; 
        System.out.println(result);

        //Multiplication
        result = num1 * num2; 
        System.out.println(result);

        //Division
        result = num1 / num2; 
        System.out.println(result);

        //Modulus
        result = num1 % num2; 
        System.out.println(result);

        //compound addition
        result = num1 += 5; 
        System.out.println(result);

        //Compound subtraction
        result = num1 -= 5; 
        System.out.println(result);

        //Compound multiplication
        result = num1 *= 5; 
        System.out.println(result);

        //Compound division
        result = num1 /= 5; 
        System.out.println(result);

        //Compound modulus
        result = num1 %= 5; 
        System.out.println(result);
    }    
}
